package com.napier.sem;

/**
 * Represents a country
 */
public class Country
{
    /**
     * Country code
     */
    public String code;
    /**
     * Country name
     */
    public String name;
    /**
     * Country's continent
     */
    public String continent;
    /**
     * Country's region
     */
    public String region;
    /**
     * Country's surface area
     */
    public Float surfaceArea;
    /**
     * Country's year of independence (can be null)
     */
    public Integer indepYear;
    /**
     * Country's population
     */
    public Integer population;
    /**
     * Country's average life expectancy
     */
    public Float lifeExpectancy;
    /**
     * Country's gross national product
     */
    public Float GNP;
    /**
     * Country's old gross national product (can be null)
     */
    public Float GNPOld;
    /**
     * Country's local name
     */
    public String localName;
    /**
     * Country's government form
     */
    public String GovernmentForm;
    /**
     * Country's head of state name
     */
    public String headOfState;
    /**
     * Country's capital city name
     */
    public Integer capital;
    /**
     * Country's code 2
     */
    public String code2;
}
